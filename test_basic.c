/* Test 1: Basic Program - Variables and Arithmetic */

int main() {
    int x;
    int y;
    int result;

    x = 10;
    y = 5;

    result = x + y;
    print(result);

    result = x - y;
    print(result);

    result = x * y;
    print(result);

    result = x / y;
    print(result);

    return 0;
}
